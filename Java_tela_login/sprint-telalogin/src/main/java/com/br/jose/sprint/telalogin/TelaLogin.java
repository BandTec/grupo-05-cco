        // TODO add your handling code here:
        BasicDataSource dataSource = new BasicDataSource();
        dataSource.setDriverClassName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        dataSource.setUrl("jdbc:sqlserver://humildificadores"
                + ".database.windows.net:1433;"
                + "database=bancohumildificadores;encrypt=true;"
                + "trustServerCertificate=false;"
                + "hostNameInCertificate=*.database.windows.net;");

        dataSource.setUsername("adminlocal");
        dataSource.setPassword("Safadinhos123@");

        JdbcTemplate jdbcTemplate = new JdbcTemplate(dataSource);
        
        String email = tfCampoEmail.getText();
        String senha = tfCampoSenha.getText();

        List<ConexaoBanco> login = jdbcTemplate.query(
                "select * from cliente where email = ? and senha = ?",
                new BeanPropertyRowMapper(ConexaoBanco.class), email, senha);
        
        //for (ConexaoBanco loginTabela : login) {
        //    System.out.println("Login: "+loginTabela.getEmail());
        //    if (login.isEmpty()) {
        //        mensagemErro = "Email e senha incorretos. Tente novamente.";
        //    } else {
        //        mensagem = "Login realizado com sucesso!";
        //        saudacao = "Bem vindo, aluno";
        //    }
        //}
        
        if (login.isEmpty()) {
            tfResultado.setText("Falha de autenticação!");
            tfResultado.setForeground(Color.RED);
        } else {
            
            ConexaoBanco registro = login.get(0);
            
            String saudacao = "Conectado com sucesso!";
            String texto = 
                    String.format("Bem vindo(a), %s.", registro.getNome());
            tfResultado.setText(texto);
            tfResultado.setForeground(Color.BLUE);
            lbSaudacao.setText(saudacao);
            
        }

